a = 5
b = 10
c = 15
print(a, b, c)
print(type(a))

# Float type
x = 2.4
y = 5.0
print(type(y))

# Complex type
d = 2 + 2j
print(d)
print(type(d))

# Binary type
e = 0B1010
print(e)
print(type(e))

# Hexadecimal
